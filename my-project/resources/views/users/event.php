    <h1 class="font-bold text-3xl">Ferrari Cavalcade 2012.</h1>
    <br>
    <p>From 28 to 30 June over 100 clients of the Prancing Horse from all over the world will discover the Emilia region on the roads of the legendary races of the past.</p>
<br>
<img src="images/a.png" alt="">
<br>
<p>The places are those between Emilia Romagna and Tuscany, linked to the birth of the Maranello firm and to the epic road races of the past. Mille Miglia and Circuito del Savio, but also the current permanent tracks on which Ferrari raced and won such as the Imola and Mugello circuits and the home one, Fiorano, where all the Prancing Horse cars are developed.

The tour will start from Bologna, heading towards the "Dino and Enzo Ferrari" circuit on the banks of the Santerno river, where three skill tests will be held, valid for the classification of the regularity race. The first day will be dedicated to the flat roads: in addition to Lugo, home of the Baracca family where the Prancing Horse will be celebrated, we will travel along the roads of the Po Delta and visit the ancient Abbey of Pomposa to return to the Ferrara of the Estensi through the suggestive roads that cut the fields of the Pianura Padana.</p>
<br>
<br>
<h1 class="text-3xl font-bold">2015 Ferrari Cavalcade in Rome.</h1>
<br>
<p>This year at its fourth edition, the Cavalcade is a special event organized directly by Ferrari to give the possibility to its most important members to drive all the brand new Ferraris on the wonderful scenary of the Italian roads. This Gran Turismo event is reserved to the most important owners from all over the world, and to take part at the Cavalcade you need to have 5 different Ferraris in your garage.</p>
<img src="images/c.png" alt="">
<p>The Ferrari Cavalcade 2015 was held in the eternal city, Rome, touching the most beautiful places of the world driving across the "Castelli Romani", the Colosseo, the Ara Pacis, Vatican City and having an experience on the famous race track of Circuito di Vallelunga, one of the hardest Italian circuit.</p>
<br>
<br>
<h1 class="text-3xl font-bold">Ferrari Cavalcade 2016 in Venice.</h1>
<br>
<p>Reds in the Lagoon. Over 100 Ferraris driven by customers of 35 different nationalities are the stars of the 2016 Ferrari Cavalcade.</p>
<img src="images/d.png" alt="">
<p>The event, now in its fifth edition, will take place from 23 to 26 June in Venice and its magnificent surroundings. As per tradition, the Cavalcade runs along some of the most beautiful roads in Italy, passing through places of striking beauty.

The excellences of Belpaese. The first edition of Cavalcade took place in 2012 in Emilia Romagna, in 2013 in Tuscany, in 2014 in Sicily, while last year it was the turn of Rome.</p>
